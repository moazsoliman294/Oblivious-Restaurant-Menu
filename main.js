//DOMContentLoaded .

const sectioncenter=document.querySelector(".card-ss");
const btncontainer=document.querySelector(".btn-container");

window.addEventListener("DOMContentLoaded",function(){
    displaymenuitem(menu);
    displaymenubtn();
});

 //map that displays all items of the menu

function displaymenuitem(menuitem){

    let displaymenu = menuitem.map(function (item) {

        return`
         <div class="cards">
    <div class="card">
    <img class="moaz" src="${item.img}" alt="${item.title}">
    <p id="ww">${item.desc}</p>
      
      <h1>${item.price}</h1>
    <div class="card2"></div>
    <h5 id="hh">${item.title}</h5>
</div>
</div>
        `;
    });

    displaymenu=displaymenu.join("");
    //inner HTML added
    sectioncenter.innerHTML=displaymenu;
}
 //Reduce used to filter the items in the menu with respect to their categories

function displaymenubtn(){
    const categories=menu.reduce(
        function (values,item) {
            if(!values.includes(item.category)){
                values.push(item.category);
            }
            return values;
        },
        ["all"]
    );


    const categorybtns=categories
    .map(function (category) {
         return `
         <div class="fillter"> <a href="#card-ss"><button  type="button" class="filter-btn" data-id=${category}>
            ${category}
          </button> </a>
          </div>`;
    })
    .join("");

    btncontainer.innerHTML=categorybtns;

    const filterbtns=btncontainer.querySelectorAll(".filter-btn");
    console.log(filterbtns);
 
         filterbtns.forEach(function (btn) {
         btn.addEventListener("click", function (e) {
            const category= e.currentTarget.dataset.id;
            const menucategory= menu.filter(function (menuitem) {
                if (menuitem.category===category) {
                    return menuitem;
                }
            });
            if (category==="all") {
                displaymenuitem(menu);
            }else{
                displaymenuitem(menucategory);
            }
        });
    });
};

const items=document.querySelectorAll('.card');

